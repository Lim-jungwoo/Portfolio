#include "libft.h"

int	ft_strchr(char *s, int c)
{
	int		i;

	i = 0;
	if (!*s && c == 0)
		return (-1);
	while (*s)
	{
		if ((int)*s == c)
			return (i);
		s++;
		i++;
	}
	return (-1);
}
